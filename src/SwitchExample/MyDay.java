package SwitchExample;

public class MyDay {

	public static void main(String[] args) {
	int Person=3;
	switch(Person) {
	case 2:
		System.out.println("Today is Sunday");
		break;
	case 4:
		System.out.println("I have to go to work");
		break;
	case 5:
		System.out.println("My dad is coming");
		break;
	default:
			System.out.println("I need a Rest");
	}

	}

}
